//: ## Traversal
//: One of the features of binary search trees is the fact that it is always sorted. To extract the nodes in ascending sorted order we use an inorder traversal!

//: ## In-Order Traversal
//: In a traversal, we want to retrieve all nodes in a certain order. From the root, We will 'visit' all nodes beginning with the left, then root, then right node. If any node has a child, we advance to that node and repeat the same process before moving on to the other branches.

